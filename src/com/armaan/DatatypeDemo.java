package com.armaan;

public class DatatypeDemo {

	public static void main(String[] args) {
		byte a1=56;
		short s1=156;
		int i1=1658;
		long d1=455475L;
		float f1=10.56f;
		double s2=20.56;
		char c1='a';
		boolean b=false;
		String n="Rashmika";
		System.out.println("byte default value:"+a1);
		System.out.println("short default value:"+s1);
		System.out.println("int default value:"+i1);
		System.out.println("long default value:"+d1);
		System.out.println("float default value:"+f1);
		System.out.println("double default value:"+s2);
		System.out.println("char default value:"+c1);
		System.out.println("boolean default value:"+b);
		System.out.println("String default value:"+n);
	}

}
